package org.cru.entities;

public class Bucket 
{
	private String name;
	private double hours;
	
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public double getHours()
	{
		return hours;
	}
	
	public void setHours(double hours)
	{
		this.hours = hours;
	}
}
